    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
   try {
       String TM1 = TM.getText();
       String ND1 = ND.getText();
       String G2 = G.getText();
       String P1 = P.getText();
       String T1 = G1.getText(); 
       if(TM1.equals("") || ND1.equals("") || G2.equals("") || P1.equals("") || T1.equals(""))
        {
           JOptionPane.showMessageDialog(rootPane, "Trống");
           new TINHTIEN().setVisible(true);
           this.dispose();
        } else 
        {
           int a = Integer.valueOf(G2);
           int b = Integer.valueOf(P1);
           int c = Integer.valueOf(T1);
           float d = (a*60)+b;
           float e = d/60;
           float tongtien= e*c;
           TT.setText(String.valueOf(tongtien));
           ConnectDB g = new ConnectDB();
           Connection conn = g.ConnectDB();
           PreparedStatement pst = null;
           try {
               pst = conn.prepareStatement("insert into LICHSU value (?,?,?,?)");
               pst.setString(1, TM1);       
               pst.setString(2,ND1);
               pst.setString(3,String.valueOf(d));
               pst.setString(4, String.valueOf(tongtien));
               int chk = pst.executeUpdate();
               if (chk >0)
               {
                   JOptionPane.showMessageDialog(this, "Đã Tính tiền!");
               }
               else 
               {
                   JOptionPane.showMessageDialog(rootPane, "Lỗi Tính Tiền! Vui lòng xem lại!");
               }
               conn.close();
           } catch (SQLException ex) {
               JOptionPane.showMessageDialog(rootPane, "Lỗi Tên máy không tồn tại!");
           }
       }
       }
   catch (Exception v)
   {
       JOptionPane.showMessageDialog(rootPane, "Nhập đúng định dạng!");
   }    
    }//GEN-LAST:event_jButton1ActionPerformed

